class Transporte:
    def __init__(self, velocidade, marca):
        self.velocidade = velocidade
        self.marca = marca

    def Mover(self):
        print("O transporte est치 se movendo")
    
class Carro(Transporte):
    def __init__(self, velocidade, marca):
        super().__init__(velocidade, marca)
    def Mover(self):
        print("O carro est치 rodando pela estrada")
    
class Moto(Transporte):
    def __init__(self, velocidade, marca):
        super().__init__(velocidade, marca)
    def Mover(self):    
        print("A moto est치 acelerando")
    
class Bicicleta(Transporte):
    def __init__(self, velocidade, marca):
        super().__init__(velocidade, marca)
    def Mover(self):
        print("A bicicleta est치 pedalando")

print("----RESULTADO FINAL----")    
moto = Moto("120KM/h", "YAMAHA")
moto.Mover()

carro = Carro("140KM/h", "FERRARI")
carro.Mover()

bicicleta = Bicicleta("50KM", "KSW")
bicicleta.Mover()